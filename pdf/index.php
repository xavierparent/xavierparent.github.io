 <p> I am a senior postdoctoral researcher, working as Principal Investigator in the project &#8220;Axiomatizing normative conditional reasoning&#8221;  funded by <a href="https://www.fwf.ac.at/">FWF</a> (Austrian Science Fund) under the 
(now discontinued)
Lisa Meitner programme, and hosted  at the <a href="https://informatics.tuwien.ac.at/orgs/e192"> Institute of Logic and 
Computation</a> of the Faculty of Informatics at TU Wien, within A. Ciabattoni's 
<a href="https://www.logic.at/">Theory and Logic</a> research group. 
<p>
My research topic is
Logic and Artificial Intelligence, more specifically Knowledge Representation and Reasoning, with a 
special emphasis on normative 
reasoning (deontic logic). 

I took the PhD degree from the University of Aix-Marseille I,
 France. After that, I worked as a research associate at the
 <a href="http://www.kcl.ac.uk/nms/depts/informatics/index.aspx">
 Department of Computer Science</a> of the King's College London, UK,
 and subsequently at the
 <a href=https://wwwen.uni.lu/recherche/fstm/dcs>
 Computer Science Department</a> of the University of Luxembourg.
</p>

<p> I am a co-editor of the two volumes of the <em> Handbook of Deontic Logic and
	  Normative Systems</em>, and a co-author of the textbook <em>Introduction to Deontic Logic and Normative Systems</em> . I was the general conference chair
	  of <a href="http://2018.ruleml-rr.org">RuleML+RR 2018 </a>
	  and a PC co-chair of DEON 2014.
	  </p>My CV is <a href="/pdf/cv-xav-final29Jan.pdf"> here</a>. <p>
